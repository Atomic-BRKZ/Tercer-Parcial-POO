    private void jBInicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBInicioActionPerformed
        // TODO add your handling code here:
        if(busuarios.exists())
        {
            jdlogin jdl = new jdlogin(null, true);
            jdl.setVisible(true);
                    
            String usuario= jtUsuario.getText();
            String password= jtPassword.getText();
           
            this.deserializarUsuarios();
            String rol= this.buscarUsuario(usuario, password);
           
            if(rol.length()>0)
            {
             principal p = new principal();
             p.enviarUsuarios(lusuarios, usuario, rol);
             p.setExtendedState(JFrame.MAXIMIZED_BOTH);
             p.setVisible(true);
            }else
                JOptionPane.showMessageDialog(null, "Usuario incorrecto");
        }
        else
        {
        // abrir jdialog para crear usuario
           nuevoUsuario nu = new nuevoUsuario(this, true);
           nu.iniciar(this);
           nu.setVisible(true);
           
        }
    }//GEN-LAST:event_jBInicioActionPerformed

    public String buscarUsuario(String u, String p)
    {
      String rol="";
      for(Usuario usu: lusuarios)
      {
         if(usu.getUsuario().compareTo(u)==0 && usu.getPassword().compareTo(encriptasha256.convertirSHA256(p))==0)
         {
             rol= usu.getRol();
             break;
         }
      }
      return rol;
    }
            
    public void serializarUsuarios()
    {
       FileOutputStream archivo;
       ObjectOutputStream salida;
       try{
       
           archivo= new FileOutputStream("usuarios.dat");
           salida= new ObjectOutputStream(archivo);
           if(salida!=null)
               salida.writeObject(lusuarios);
           
           salida.close();
           
       }catch(Exception e1)
       {
         System.out.println("Error "+ e1.getMessage());
       }
    }
    
     public void deserializarUsuarios()
    {
       FileInputStream archivo;
       ObjectInputStream entrada;
       try{
       
           archivo= new FileInputStream("usuarios.dat");
           entrada= new ObjectInputStream(archivo);
           if(entrada!=null)
              lusuarios=(ArrayList<Usuario>) entrada.readObject();
           
           entrada.close();
           
       }catch(Exception e1)
       {
         System.out.println("Error "+ e1.getMessage());
       }
    }
