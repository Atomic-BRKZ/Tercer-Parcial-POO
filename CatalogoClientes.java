    private void creaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_creaActionPerformed
        // TODO add your handling code here:
        try
        {
            String nombre = jtnombre.getText();
            String cedula = jtCode.getText();
            
            
            Clientes client= new Clientes(nombre, cedula);
            
            jprincipal.clclientes.add(client);
            jprincipal.serializarClientes();
            
           Object[] fila= new Object[3];
           fila[0]= client.getCedula();
           fila[1]= client.getNombre();
           
           
           dft1.addRow(fila);
        }
        catch(Exception e1)
        {
          JOptionPane.showMessageDialog(null, "Error al crear objeto");
        }

    }//GEN-LAST:event_creaActionPerformed

