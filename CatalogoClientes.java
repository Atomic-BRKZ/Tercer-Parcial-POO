    public void iniciar(principal p)
    {
        this.jprincipal =p;
        jprincipal.deserializarClientes();
        
     try{
        dft1 = (DefaultTableModel)jCliente1.getModel();
        
        
        int conta=0;
        for(Clientes u: jprincipal.clclientes)
        {
            conta++;
           Object[] fila= new Object[2];
           fila[0]= u.getNombre();
           fila[1]= u.getCedula();
          
                  
           
           dft1.addRow(fila);
        }
        
        }catch(Exception e1)
        {
        JOptionPane.showMessageDialog(null, e1.getMessage());
        }
    }
    
    

