    private void creActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_creActionPerformed
        // TODO add your handling code here:
        try
        {
            String nombre= jtNombre.getText();
            String cantidad = jtCantidad.getText();
            String precio= jtPrecio.getText();
           
            
            Productos u= new Productos(nombre, cantidad, precio);

            jprincipal.pdproductos.add(u);
            jprincipal.serializarProductos();

            Object[] fila= new Object[3];
            fila[0]= u.getNombre();
            fila[1]= u.getCantidad();
            fila[2]= u.getPrecio();

            dft.addRow(fila);
        }
        catch(Exception e1)
        {
            JOptionPane.showMessageDialog(null, "Error al crear objeto");
        }
    }//GEN-LAST:event_creActionPerformed

