    public void iniciar(principal p)
    {
     this.jprincipal =p;
     jprincipal.deserializarProductos();
     try{
        dft = (DefaultTableModel)jTable1.getModel();
        
        
        int conta=0;
        for(Productos u: jprincipal.pdproductos)
        {
            conta++;
           Object[] fila= new Object[3];
           fila[0]= u.getNombre();
           fila[1]= u.getCantidad();
           fila[2]= u.getPrecio();
          
                  
           
           dft.addRow(fila);
        }
        
        }catch(Exception e1)
        {
        JOptionPane.showMessageDialog(null, e1.getMessage());
        }
    }
    

    
